public class estruturaFor {
    public static void main(String[] args) {
        //Aqui vamos basicamente contar sem precisar escrever cada variavel ou número a ela.

        for(int contador = 1; contador <= 50; contador++){ //dentro do nosso for, indicamos nossa variavel
            //indicamos o valor dessa variável e separamos por ;
            // indicamos que o loop irá até 50.
            //por fim, indicamos que somará +1 a cada loop do programa.
            System.out.println("Contador de número ate 50: "+contador);
        }
    }
    
}
